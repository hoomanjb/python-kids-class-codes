import turtle

s = turtle.getscreen()
turtle.title('new project')
turtle.bgcolor('white')
t = turtle.Turtle()

t.pensize(7)
t.shapesize(2)
t.speed(2)
t.color('blue')
t.begin_fill()
t.goto(100, 0)
t.goto(100, 100)
t.goto(0, 100)
t.goto(0, 0)
t.end_fill()
t.color('red')
t.begin_fill()
t.goto(50, 50)
t.goto(0, 100)
t.end_fill()
t.color('green')
t.begin_fill()
t.goto(-50, 50)
t.home()
t.end_fill()
t.color('blue')
t.begin_fill()
t.goto(50, 50)
t.goto(100, 0)
t.end_fill()
t.color('yellow')
t.begin_fill()
t.goto(50, -50)
t.home()
t.end_fill()
t.color('blue')
t.goto(50, 50)
t.color('orange')
t.begin_fill()
t.goto(100, 100)
t.goto(100, 0)
t.goto(50, 50)
t.goto(100, 100)
t.end_fill()
t.color('pink')
t.begin_fill()
t.goto(150, 50)
t.goto(100, 0)
t.goto(100, 100)
t.end_fill()
t.color('purple')
t.begin_fill()
t.goto(50, 150)
t.goto(0, 100)
t.goto(100, 100)
t.end_fill()

turtle.mainloop()
